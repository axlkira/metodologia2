    //esta funcion actualiza
    public function fm_actualizarpregunta($mfolio,$midintegrante,$mvalorc10p4,$mc10p4cual)
    {
        $this->db->query('call spactualizarcapitulo10p4('.$mfolio.','.$midintegrante.',"'.$mvalorc10p4.'","'.$mc10p4cual.'");');
    }    
    
       
}      