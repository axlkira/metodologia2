<?php

class M_sincronizacion_casoexitoso extends CI_Model {

    public function __construct() {
        parent::__construct();
        $this->load->database();
    }
    
    //esta funcion ejecuta el sp para cargar las ayudas de la pregunta
    public function fm_sin_casoexitoso()
    {
        $query = $this->db->query('call sp6sincrocasoexitoso();');//se coloca el codigo de la ayuda
        $resultado = $query->result();
        $query->next_result(); //NO SE TOCAN Estas dos funciones permiten realizar varias consultas en el mismo controlador.
        $query->free_result(); //NO SE TOCAN  Estas dos funciones permiten realizar varias consultas en el mismo controlador.
        return $resultado;        
    }

    public function fm_sin_casoexitosook()// falta para que los muchachos le pongan bandera
    {
