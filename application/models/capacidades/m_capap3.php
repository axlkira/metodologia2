    //esta funcion actualiza
    public function fm_actualizarpregunta($mfolio,$mcapap3o1,$mcapap3o2,$mcapap3o3,$mcapap3o4,$mcapap3o5)
    {
        $this->db->query('call spactualizarcapap3('.$mfolio.',"'.$mcapap3o1.'","'.$mcapap3o2.'","'.$mcapap3o3.'","'.$mcapap3o4.'","'.$mcapap3o5.'");');
    }    
    
    
}      